package com.priceking.entity;

/**
 * Product Entity that contains all information about a single Categories
 * 
 * @author DEVEN
 * 
 */
public class Categories {
	private String name;
	private int icon;

	public Categories() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getIcon() {
		return icon;
	}

	public void setIcon(int icon) {
		this.icon = icon;
	}

}
